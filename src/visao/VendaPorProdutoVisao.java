    private void btnfiltrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnfiltrarActionPerformed
        String valor = txtCodigo.getText().trim();
        if (valor.isEmpty()) {
            if (dataInicial.getDate() == null && dataFinal.getDate() == null) {
                JOptionPane.showMessageDialog(this, "Informe as datas!");
            } else {
                pedidoItemList.clear();
                //pedidoItemList.addAll(PedidoItemControle.buscaDinamica(valor.isEmpty() ? 0 : Integer.parseInt(valor),
                        //dataInicial.getDate(), dataFinal.getDate(), cliente == null ? 0 : cliente.getpCodigo()));
            }
        } else {

            if (Tools.isInt(valor)) {
                pedidoItemList.clear();
                if (PedidoControle.buscaPorCodigo(Integer.parseInt(valor)) != null) {

                    //pedidoItemList.add(PedidoControle.buscaPorCodigo(Integer.parseInt(valor)));

                }
                dataInicial.setDate(null);
                dataFinal.setDate(null);
                txtCodigo.setText("");
                txtCliente.setText("");
            } else {
                JOptionPane.showMessageDialog(this, "Informe um código válido!");
            }
        }
        calculaTotal();
    }//GEN-LAST:event_btnfiltrarActionPerformed

