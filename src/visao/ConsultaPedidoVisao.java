    private void btnExportarPDFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExportarPDFActionPerformed
        if (exportarPorData && exportarPorDataCliente) {
            Map<String, Object> parametros = new HashMap<>();
            parametros.put("dataInicial", Tools.dateFormat(dataInicial.getDate(), "dd-MM-yyyy"));
            parametros.put("dataFinal", Tools.dateFormat(dataFinal.getDate(), "dd-MM-yyyy"));
            parametros.put("cliente", txtCliente.getText().trim());
            report("ReportPedidoPorDataCliente.jasper", parametros);
        } else if (exportarPorData) {
            Map<String, Object> parametros = new HashMap<>();
            parametros.put("dataInicial", Tools.dateFormat(dataInicial.getDate(), "dd-MM-yyyy"));
            parametros.put("dataFinal", Tools.dateFormat(dataFinal.getDate(), "dd-MM-yyyy"));
            report("ReportPedidoPorData.jasper", parametros);

        }
    }//GEN-LAST:event_btnExportarPDFActionPerformed

    private void report(String reportName, Map<String, Object> parametros) {
        try {
            List<Map<String, Object>> dataSourceList = new ArrayList<>();

            pedidoList.forEach((item) -> {
                Map<String, Object> row = new HashMap<>();
                row.put("pedidoVendaCodigo", item.getPedidoVendaCodigo());
                row.put("pedidoVendaData", item.getPedidoVendaData());
                row.put("pedidoVendaVencimento", item.getPedidoVendaVencimento());
                row.put("pedidoVendaCliente", item.getCliente().toString());
                row.put("pedidoVendaTotalProdutos", item.getPedidoVendaTotalProdutos());
                row.put("pedidoVendaTotalPedido", item.getPedidoVendaTotalPedido());
                row.put("pedidoVendaComprador", item.getPedidoVendaComprador());
                dataSourceList.add(row);
            });

            JRDataSource dataSource = new JRBeanCollectionDataSource(dataSourceList);

            JasperPrint jasperPrint = JasperFillManager.fillReport("reports\\" + reportName, parametros, dataSource);
            //JasperFillManager.fillReportToFile("reports\\" + reportName,path+reportName+".pdf", parametros, dataSource);
            JasperExportManager.exportReportToPdfFile(jasperPrint, "" + ConsultaPedidoVisao.path + "CONSULTAPEDIDOS" + Tools.dateFormat(new Date(), "yyyy-MM-dd") + ".pdf");
            //JasperViewer view = new JasperViewer(jasperPrint, false);
            //view.setVisible(true);
            JOptionPane.showMessageDialog(this, "Salvo em: " + ConsultaPedidoVisao.path + "CONSULTAPEDIDOS" + Tools.dateFormat(new Date(), "yyyy-MM-dd") + ".pdf");
        } catch (JRException ex) {
            JOptionPane.showMessageDialog(this, "Não foi possível exportar o PDF!\nErro: " + ex.getMessage());
        }
    }

    private void calculaTotal() {
        total = new BigDecimal("0");
        totalProdutos = new BigDecimal("0");

        pedidoList.forEach((item) -> {
            total = total.add(item.getPedidoVendaTotalPedido());
            totalProdutos = totalProdutos.add(item.getPedidoVendaTotalProdutos());
        });
        txtTotalPedidos.setText(total.toString());
        txtTotalProdutos.setText(totalProdutos.toString());
    }

    private void setCliente() {
        cliente = BuscaClienteVisao.cliente;
        txtCliente.setText(cliente.getNomeRazaoSocial());

    }

    private void atualizaTabela() {
        pedidoList.clear();
        pedidoList.addAll(PedidoControle.buscaTodos());
        calculaTotal();
    }

