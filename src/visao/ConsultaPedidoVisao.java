    private void btnExportarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExportarActionPerformed
        if (pedidoList.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Não há registros para exportar!");
        } else {
            try {
                Tools.exportTable(tablePedidos, new File(ConsultaPedidoVisao.path + "CONSULTAPEDIDOS" + Tools.dateFormat(new Date(), "yyyy-MM-dd") + ".xls"));
                JOptionPane.showMessageDialog(this, "Salvo em: " + ConsultaPedidoVisao.path + "CONSULTAPEDIDOS" + Tools.dateFormat(new Date(), "yyyy-MM-dd") + ".xls");
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(this, "Não foi possível exportar a planilha!\nErro: " + ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnExportarActionPerformed

