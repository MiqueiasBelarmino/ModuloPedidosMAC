    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        if (txtCodigo.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Informe o codigo do pedido");
        } else {
            if ((!txtCodigo.getText().trim().isEmpty())) {
                PedidoVisao.pedido = PedidoControle.buscaPorCodigo(Integer.parseInt(txtCodigo.getText().trim()));
                if (PedidoVisao.pedido != null) {
                    if (PedidoVisao.pedido.getPedidoVendaCodigo() > 0) {
                        preencheCampos(PedidoVisao.pedido);
                        if (PedidoControle.permiteAlterarExcluir(codigoUsuario)) {
                            novo = false;
                            habilitaComponentes(true);
                            flagNovoAlterarCliente = true;
                            fechar = false;
                            pedidoItemControle = new PedidoItemControle();
                            txtCodigo.setEnabled(false);
                        } else {
                            JOptionPane.showMessageDialog(this, "Voc√™ n√£o possui permiss√£o para altere√ß√£o!");
                        }
                    }
                }
            }

        }
    }//GEN-LAST:event_btnAlterarActionPerformed

