    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        int qtd = txtQuantidade.getText().trim().isEmpty() ? 0 : Integer.parseInt(txtQuantidade.getText().trim());
        String preco = txtPrecoUnitario.getText().trim().isEmpty() ? "0" : txtPrecoUnitario.getText().trim();
        if (txtProduto.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Selecione um produto!", null, JOptionPane.INFORMATION_MESSAGE);
        } else if (qtd == 0) {
            JOptionPane.showMessageDialog(null, "Informe uma quantidade v√°lida!", null, JOptionPane.INFORMATION_MESSAGE);
        } else if (preco.equals("0")) {
            JOptionPane.showMessageDialog(null, "Informe um pre√ßo v√°lido!", null, JOptionPane.INFORMATION_MESSAGE);
        } else {

            pedidoItem = new PedidoItem();
            pedidoItem.setProduto(produtoPedido);
            pedidoItem.setPedVendasProdQtde(qtd);
            pedidoItem.setPedVendasProdValor(new BigDecimal(preco));
            pedidoItem.setGrupoCodigo(pedidoItem.getProduto().getGrupoEstCodigo());
            pedidoItem.setPedVendasProdTotal((pedidoItem.getPedVendasProdValor().multiply(new BigDecimal("" + pedidoItem.getPedVendasProdQtde()))));
            pedidoItem.setPedidoVendaCodigo(pedido.getPedidoVendaCodigo());
            pedidoItem.setPedVendasProdIPI(new BigDecimal(txtIPI.getText().trim().isEmpty() ? "0" : txtIPI.getText().trim()));
            pedidoItem.setEmpCodigo(2);

            validaProduto(pedidoItem);
            //pedidoItemList.add(pedidoItem);
            sub = new BigDecimal("0");
            pedidoItemList.forEach((i) -> {
                sub = sub.add(i.getPedVendasProdTotal());
            });
            txtSubtotal.setText(sub.toString());

            clearProduct();
            calculaTotal();
            //updateTable();
        }
    }//GEN-LAST:event_btnAdicionarActionPerformed

