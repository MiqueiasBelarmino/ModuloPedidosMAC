    private void btnComfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnComfirmarActionPerformed
        if (pedido != null) {
            if (txtCliente.getText().trim().isEmpty() || clientePedido == null) {
                JOptionPane.showMessageDialog(null, "Selecione um Cliente");
                txtCliente.requestFocus();
            } else if (dateVenda.getDate() == null) {
                JOptionPane.showMessageDialog(null, "Informe a data da venda");
                dateVenda.requestFocus();
            } else if (dateVencimento.getDate() == null) {
                JOptionPane.showMessageDialog(null, "Informe a data do vencimento");
                dateVencimento.requestFocus();
            } else if (txtVendedor.getText().trim().isEmpty() || funcionario == null) {
                JOptionPane.showMessageDialog(null, "Selecione um Vendedor");
                txtVendedor.requestFocus();
            } else {

                populaPedido();

                try {

                    if (novo) {
                        pedidoControle.save(pedido);
                        pedidoItemList.forEach((item) -> {
                            pedidoItemControle.save(item);
                        });
                    } else {
                        pedidoControle.update(pedido);
                        pedidoItemList.forEach((item) -> {
                            pedidoItemControle.update(item);
                        });
                        pedidoItemRemover.forEach((item) -> {
                            pedidoItemControle.delete(item);
                        });
                    }
                    int op = JOptionPane.showConfirmDialog(this, "Deseja imprimir/visualizar o pedido?");
                    if (op == 0) {
                        report(pedido.getPedidoVendaCodigo());
                    }

                    limpaObjetos();
                    limpaCampos();
                    habilitaComponentes(false);

                    flagNovoAlterarCliente = false;
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Falha ao gravar as informa√ß√µes!\nErro:" + e, null, JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_btnComfirmarActionPerformed

